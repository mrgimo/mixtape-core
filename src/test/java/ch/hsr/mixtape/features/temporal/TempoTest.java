	private static final String TEST_DATA_FILE = "src/test/resources/AubioTestData.test";

	private static final int SAMPLE_RATE_IN_HZ = 44100;

	private static final int WINDOW_SIZE = 1024;

	private static final int HOP_SIZE = 512;

	private double[] samples;

	private int resultsSize;

	private Tempo tempo;

	private double[] extractedBPMs;

	private PhaseVocoder phaseVocoder = new PhaseVocoder(WINDOW_SIZE, HOP_SIZE);

	@Before
	public void setUp() {
		loadTestData();

		resultsSize = (int) Math.ceil((double) (samples.length / HOP_SIZE)) + 1;

		tempo = new Tempo(SpectralDescriptionType.HIGH_FREQUENCY_CONTENT,
				resultsSize, HOP_SIZE, SAMPLE_RATE_IN_HZ);

		extractedBPMs = new double[resultsSize];
	}

	private void loadTestData() {
		try {
			Path path = Paths.get(TEST_DATA_FILE);
			BufferedReader br = Files.newBufferedReader(path,
					Charset.forName("UTF-8"));

			String readLine;
			while ((readLine = br.readLine()) != null)
				if (!readLine.isEmpty())
					break;

			String[] split = readLine.split(",");

			samples = new double[split.length];
			for (int i = 0; i < split.length; i++)
				samples[i] = Double.parseDouble(split[i]);
		} catch (IOException e) {
			e.printStackTrace();
		}
	}

	@Test
	public void testTempoExtraction() {
		int currentWindow = 0;
		int index = 0;

		double[] dataold = new double[WINDOW_SIZE - HOP_SIZE];
		double[] data = new double[WINDOW_SIZE];
		while (currentWindow < samples.length) {
			double[] datanew = Arrays.copyOfRange(samples, currentWindow,
					currentWindow + WINDOW_SIZE);
			currentWindow += HOP_SIZE;

			System.arraycopy(dataold, 0, data, 0, WINDOW_SIZE - HOP_SIZE);
			System.arraycopy(datanew, 0, data, WINDOW_SIZE - HOP_SIZE, HOP_SIZE);
			System.arraycopy(data, HOP_SIZE, dataold, 0, WINDOW_SIZE - HOP_SIZE);

			tempo.extractTempo(false, phaseVocoder.computeSpectralFrame(data));
			extractedBPMs[index] = tempo.getBPM();
		}

		Assert.assertArrayEquals(EXPECTED_BPMS, extractedBPMs, 0.000000000001);
	}

